void StaticModel::Update(float dt)
{
    Actor::Update(dt);


    // check health
    if (m_health <= 0.0f) {
        m_destroyed = true;
    }
}

void StaticModel::OnCollision(Actor* other)
{
    //Player* p = dynamic_cast<Player*>(other);

    if (other->m_tag == "PlayerBullet") {
        m_health -= 5.0f;
    }
}
